    private void dropStudentButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_dropStudentButtonActionPerformed
        // TODO add your handling code here:
        
        dropStudentTextArea.setText("");
        
        String[] studentName = dropStudentAdminComboBox.getSelectedItem().toString().split(",");
        String studentID = studentName[1];
        String result1 = studentName[0] + " has been dropped from the list of students.\n";
        dropStudentTextArea.append(result1);
        String result2 = "";
        
        ArrayList<String> allSemesters = SemesterQueries.getSemesterList();
        java.sql.Timestamp currentTimestamp = new java.sql.Timestamp(Calendar.getInstance().getTime().getTime());
        
        for (String semester : allSemesters)
        {
            StudentQueries.dropStudent(studentID);
            
            if (!semester.equals(""))
            {
                result2 = "\nFor semester " + semester + "\n";
            }   
                ArrayList<ScheduleEntry> allSchedule = ScheduleQueries.getScheduleByStudent(semester, studentID);
            
            for (ScheduleEntry schedule : allSchedule)
            {
                String course = schedule.getCourseCode();
                String status = schedule.getStatus();
                ArrayList<ScheduleEntry> waitlistedSchedule = ScheduleQueries.getWaitlistedStudentsByCourse(semester, course);
                ScheduleQueries.dropStudentScheduleByCourse(semester, studentID, course);

                if (status.equals("W"))
                {
                    result2 += String.format(studentName[0] + " has been dropped from the waitlist for " + course + "\n");
                }
                else
                {   
                    if (!waitlistedSchedule.isEmpty())
                    {
                        String newStudentID = waitlistedSchedule.get(0).getStudentID();
                        StudentEntry studentAll = StudentQueries.getStudent(newStudentID);
                        String firstName = studentAll.getFirstName();
                        String lastName = studentAll.getLastName();

                        ScheduleEntry student = new ScheduleEntry(semester, course, newStudentID, "S", currentTimestamp);
                        ScheduleQueries.updateScheduleEntry(semester, student);

                        result2 += String.format(studentName[0] + " has been dropped from " + course + "\n" + firstName + " " + lastName + " has been scheduled into " + course + "\n");                    
                    }
                    else
                    {
                        result2 += String.format(studentName[0] + " has been dropped from " + course + "\n");                                           
                    }
                }
            }                        
            rebuildStudentComboBoxes();
            dropStudentTextArea.append(result2);
        }
        
    }//GEN-LAST:event_dropStudentButtonActionPerformed

