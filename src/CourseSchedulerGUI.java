    public void rebuildStudentComboBoxes()
    {
        ArrayList<StudentEntry> students = StudentQueries.getAllStudents();
        ArrayList<String> allStudents = new ArrayList<>();
        ArrayList<String> allStudentsDropCourse = new ArrayList<>();
        ArrayList<String> courses = new ArrayList<>();
                
        String firstName;
        String lastName;
        String fullName;
        String fullNameID;
        String studentID;
        String course;
        for (StudentEntry student : students)
        {
            firstName = student.getFirstName();
            lastName = student.getLastName();
            studentID = student.getStudentID();
            fullName = firstName + " " + lastName;
            fullNameID = firstName + " " + lastName + "," + studentID;
            allStudents.add(fullName);
            allStudentsDropCourse.add(fullNameID);
        }
        if (!students.isEmpty())
        {
            StudentEntry studentEntry = students.get(0);
            String studentFirstID = studentEntry.getStudentID();
            ArrayList<ScheduleEntry> scheduleStudent = ScheduleQueries.getScheduleByStudent(currentSemester, studentFirstID);
            for (ScheduleEntry schedule : scheduleStudent)
                {   
                    course = schedule.getCourseCode();
                    courses.add(course);
                }
        }
        selectStudentComboBox.setModel(new javax.swing.DefaultComboBoxModel(allStudents.toArray()));
        displayScheduleComboBox.setModel(new javax.swing.DefaultComboBoxModel(allStudents.toArray()));
        dropStudentAdminComboBox.setModel(new javax.swing.DefaultComboBoxModel(allStudentsDropCourse.toArray()));
        dropCourseSelectStudentComboBox.setModel(new javax.swing.DefaultComboBoxModel(allStudentsDropCourse.toArray()));
        dropCourseSelectCourseComboBox.setModel(new javax.swing.DefaultComboBoxModel(courses.toArray()));
    }
            

