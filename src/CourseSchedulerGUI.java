    private void scheduleCourseSubmitButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_scheduleCourseSubmitButtonActionPerformed
        // TODO add your handling code here:
        
        java.sql.Timestamp currentTimestamp = new java.sql.Timestamp(Calendar.getInstance().getTime().getTime());
        
        String selectedCourse = selectCourseComboBox.getSelectedItem().toString();
        String selectedStudent = selectStudentComboBox.getSelectedItem().toString();
        
        int totalSeats = CourseQueries.getCourseSeats(currentSemester, selectedCourse);
        int enrolledSeats = ScheduleQueries.getScheduledStudentCount(currentSemester, selectedCourse);
        ArrayList<StudentEntry> students = StudentQueries.getAllStudents();

        String firstName;
        String lastName;
        String fullName; 
        String studentID = null;
        for (StudentEntry student : students) {         
            firstName = student.getFirstName();
            lastName = student.getLastName();
            fullName = firstName + " " + lastName;
            
            if (selectedStudent.equals(fullName)){
                studentID = student.getStudentID();
                
                if (enrolledSeats < totalSeats){                
                    ScheduleEntry schedule = new ScheduleEntry(currentSemester,selectedCourse,studentID,"S",currentTimestamp);
                    ScheduleQueries.addScheduleEntry(schedule);
                    scheduleCourseLabel.setText(fullName + " has been scheduled for the class " + selectedCourse + ".");
                }
                else{
                    ScheduleEntry schedule = new ScheduleEntry(currentSemester,selectedCourse,studentID,"W",currentTimestamp);
                    ScheduleQueries.addScheduleEntry(schedule);
                    scheduleCourseLabel.setText(fullName + " has been waitlisted for the class " + selectedCourse + ".");
                }
            }
        }               
        
    }//GEN-LAST:event_scheduleCourseSubmitButtonActionPerformed

